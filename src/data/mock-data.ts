/** Returns ISO date string N days in the past (positive) or future (negative) */
function daysAgo(n: number): string {
  const d = new Date("2026-03-02");
  d.setDate(d.getDate() - n);
  return d.toISOString().split("T")[0];
}

/** Returns ISO date-time string N days ago with a specific time component */
function daysAgoTime(n: number, time = "09:00:00"): string {
  return `${daysAgo(n)}T${time}Z`;
}

